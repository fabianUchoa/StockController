    private void registerBttMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerBttMouseClicked
        String email = registerEmailField.getText();
        String name = registerNameField.getText();
        String password = registerPasswordField.getText();
        String confirmPassword = registerPasswordConfirmField.getText();
        
        
        UserValidation validator = new UserValidation();
        UserController controller = new UserController();
        menuManagerOptions menu = new menuManagerOptions();
        
        if(!password.equals(confirmPassword)){
            JOptionPane.showMessageDialog(this, "As senhas n√£o coincidem", "Senha inv√°lida", JOptionPane.ERROR_MESSAGE);
        }else if(validator.loginValidator(email, password)==false){
            JOptionPane.showMessageDialog(this, "Email ou senha inv√°lidos!", "Senha inv√°lida", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                boolean manager = false;
                controller.insertUser(name, email, password, manager);
                menu.setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_registerBttMouseClicked

